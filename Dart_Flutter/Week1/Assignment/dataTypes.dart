// This program demonstrates various data types in Dart.
void main() {
  // Integer
  int age = 30;
  print('Integer: $age');

  // Double
  double pi = 3.14;
  print('Double: $pi');

  // String
  String name = 'Suleeymaan';
  print('String: $name');

  // Boolean
  bool isTrue = true;
  print('Boolean: $isTrue');

  // List
  List<int> numbers = [1, 2, 3, 4, 5];
  print('List: $numbers');

  // Map
  Map<String, int> fruits = {'apples': 5, 'bananas': 10};
  print('Map: $fruits');

  // Runes (special characters)
  String heart = "Runes: \u2764\uFE0F";
  print(heart);
}
